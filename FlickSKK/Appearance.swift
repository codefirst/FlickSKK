import UIKit

class Appearance {
    class func normalFont(size : CGFloat) -> UIFont {
      return UIFont(name: "HiraKakuProN-W3", size: size)!
    }
    class func boldFont(size : CGFloat) -> UIFont {
      return UIFont(name: "HiraKakuProN-W6", size: size)!
    }
}
